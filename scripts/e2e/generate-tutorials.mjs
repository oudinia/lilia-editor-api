#!/usr/bin/env node
/**
 * Scenario System — Phase 1 tutorial generator.
 *
 * Reads the e2e scenario catalogue and writes one `tutorial.md` per
 * scenario, plus a `manifest.json` and an index `README.md`, into
 *   ../lilia-docs/scenario-system/tutorials/<module>/<slug>/
 *
 * Phase 1 is intent-level: L1 stubs render title / module / surface /
 * intent / coverage status. Scenarios that already carry L2/L3 steps
 * render a real numbered walkthrough. Screenshots (Phase 3) and video
 * (Phase 4) land in the same per-scenario folder later.
 *
 * Output is committed to lilia-docs and pushed — GitHub renders the
 * Markdown (and, later, the images) as a browsable tutorial library.
 *
 *   Usage (Postgres connection via standard PG* env vars):
 *     PGHOST=127.0.0.1 PGPORT=5432 PGUSER=lilia PGPASSWORD=… \
 *     PGDATABASE=lilia node scripts/e2e/generate-tutorials.mjs
 *
 * See lilia-docs/scenario-system/plan.md (Phase 1).
 */

import { execFileSync } from 'node:child_process';
import { writeFileSync, mkdirSync, rmSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..', '..');
const OUT_DIR = resolve(
  REPO_ROOT, '..', 'lilia-docs', 'scenario-system', 'tutorials',
);

// One row per non-deleted scenario, with module / surface / entry-point
// names and the current version's steps.
const QUERY = `
  SELECT json_agg(t ORDER BY t.module, t.slug) FROM (
    SELECT s.slug, s.title, s.description, s.criticality, s.detail_level,
           s.review_state, s.automation_content,
           m.slug AS module, m.name AS module_name,
           surf.slug AS surface, surf.name AS surface_name,
           ep.slug AS entry_point,
           v.steps AS steps
    FROM e2e.scenario s
    JOIN e2e.module m ON m.id = s.module_id
    LEFT JOIN e2e.surface surf ON surf.id = s.target_surface_id
    LEFT JOIN e2e.entry_point ep ON ep.id = s.entry_point_id
    LEFT JOIN e2e.scenario_version v ON v.id = s.current_version_id
    WHERE NOT s.is_deleted
  ) t;
`;

// ── Coverage status, derived from detail level ──────────────────────
function coverage(level) {
  if (level === 'l3') return '✅ Covered by an executable test (L3)';
  if (level === 'l2') return '⚠️ Not automated — L2 walkthrough';
  return '⚠️ Not automated — L1 intent stub';
}

// ── Step parsing (jsonb; snake_case per the e2e DB design) ──────────
function parseSteps(raw) {
  if (!Array.isArray(raw)) return [];
  return raw
    .map((s) => {
      const o = s ?? {};
      const outcome = o.user_visible_outcome ?? o.userVisibleOutcome ?? o.expected;
      return {
        kind: String(o.step_kind ?? o.stepKind ?? o.kind ?? 'step'),
        description: String(o.description ?? o.desc ?? ''),
        outcome: typeof outcome === 'string' && outcome ? outcome : null,
      };
    })
    .filter((s) => s.description);
}

// ── Render one tutorial.md ──────────────────────────────────────────
function renderTutorial(sc) {
  const steps = parseSteps(sc.steps);
  const crumb = [sc.module, sc.surface].filter(Boolean).map((x) => `\`${x}\``).join(' › ');

  let md = `# ${sc.title}\n\n`;
  md += `${crumb}\n\n`;
  md += '| | |\n|---|---|\n';
  md += `| Criticality | **${(sc.criticality || 'p1').toUpperCase()}** |\n`;
  md += `| Coverage | ${coverage(sc.detail_level)} |\n`;
  if (sc.entry_point) md += `| Opened via | \`${sc.entry_point}\` |\n`;
  md += `| Scenario | \`${sc.slug}\` |\n\n`;

  md += '## What this verifies\n\n';
  md += `${sc.description || `_${sc.title}._`}\n\n`;

  md += '## How to test it\n\n';
  if (steps.length > 0) {
    steps.forEach((s, i) => {
      md += `${i + 1}. **${s.kind}** — ${s.description}\n`;
      if (s.outcome) md += `   _Expect: ${s.outcome}_\n`;
    });
    md += '\n';
  } else {
    md += '_No steps recorded yet — L1 intent stub._\n\n';
    md += `**Manual check:** ${sc.description || sc.title}\n\n`;
  }

  md += '---\n';
  md += `<sub>Generated by the Lilia Scenario System — do not edit by hand. `;
  md += `automation_content \`${sc.automation_content}\`. See `;
  md += `[scenario-system/plan.md](../../../plan.md).</sub>\n`;
  return md;
}

// ── Render the index README ─────────────────────────────────────────
function renderIndex(byModule, total) {
  let md = '# Lilia tutorials — scenario library\n\n';
  md += `Generated from the e2e scenario catalogue — **${total} scenarios**. `;
  md += 'One folder per scenario; each tutorial.md is derived, never ';
  md += 'hand-edited. See [vision](../vision.md) and [plan](../plan.md).\n\n';
  for (const mod of [...byModule.keys()].sort()) {
    const list = byModule.get(mod);
    md += `## ${mod} (${list.length})\n\n`;
    for (const sc of list) {
      md += `- [${sc.title}](./${mod}/${sc.slug}/tutorial.md)`;
      md += ` — ${(sc.criticality || 'p1').toUpperCase()} · ${sc.detail_level.toUpperCase()}\n`;
    }
    md += '\n';
  }
  return md;
}

// ── Run ─────────────────────────────────────────────────────────────
const raw = execFileSync('psql', ['-tAc', QUERY], {
  encoding: 'utf8',
  maxBuffer: 64 * 1024 * 1024,
});
const scenarios = JSON.parse(raw.trim() || '[]');
if (scenarios.length === 0) {
  console.error('No scenarios returned — check the PG* connection env vars.');
  process.exit(1);
}

// Fresh output tree (drops tutorials for scenarios that no longer exist).
rmSync(OUT_DIR, { recursive: true, force: true });
mkdirSync(OUT_DIR, { recursive: true });

const byModule = new Map();
const manifest = [];
for (const sc of scenarios) {
  const dir = resolve(OUT_DIR, sc.module, sc.slug);
  mkdirSync(dir, { recursive: true });
  writeFileSync(resolve(dir, 'tutorial.md'), renderTutorial(sc));

  if (!byModule.has(sc.module)) byModule.set(sc.module, []);
  byModule.get(sc.module).push(sc);
  manifest.push({
    slug: sc.slug,
    title: sc.title,
    module: sc.module,
    surface: sc.surface ?? null,
    criticality: sc.criticality,
    detailLevel: sc.detail_level,
    automationContent: sc.automation_content,
    path: `${sc.module}/${sc.slug}/tutorial.md`,
  });
}

writeFileSync(
  resolve(OUT_DIR, 'manifest.json'),
  `${JSON.stringify({ generatedAt: new Date().toISOString(), count: manifest.length, tutorials: manifest }, null, 2)}\n`,
);
writeFileSync(resolve(OUT_DIR, 'README.md'), renderIndex(byModule, scenarios.length));

console.log(`Generated ${scenarios.length} tutorials across ${byModule.size} modules.`);
console.log(`  → ${OUT_DIR}`);
